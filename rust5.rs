//datatype
fn main() {
    let x : i32 = 5;  //positive and negative integer
    println!("{x}");

    let x : f32 = 66.09;   //floating number
    println!("{x}");

    let x : u32 = 6;     //only positive
    println!("{x}");

    let x : char = 'b';    //character
    println!("{x}");

    let x : bool = true;  //boolean either true or false
    println!("{x}");
     
}
